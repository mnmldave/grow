(function() {
  
  /**
   * Parses a word and returns a tree structure of arrays and object leafs.
   *
   * @param String word containing arbitrary letters, parameters and brackets
   * @returns array of objects and arrays
   */
  exports.parse = function(word) {
    var result = [], i, c;
    
    for (i = 0; i < word.length; i++) {
      c = word.charAt(i);
    }
    
    return result;
  };
  
})();